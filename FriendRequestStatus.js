const mongoose = require("mongoose");

const FriendRequestStatusSchema = new mongoose.Schema(
  {
    senderId: {
      type: String,
      required: true,
    },
    receiverId: {
      type: String,
      required: true,
    },
    status: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

module.exports = mongoose.model(
  "FriendRequestStatus",
  FriendRequestStatusSchema
);
